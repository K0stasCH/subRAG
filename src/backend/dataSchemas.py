from pydantic import BaseModel


class Query(BaseModel):
    """
    The input question sent by the user.
    Attributes:
        query (str): The natural language question to be answered.
        session_id (str): A unique identifier for the user's session.
    """
    query: str
    session_id: str

class Response(BaseModel):
    """
    The response generated by the RAG system.
    Attributes:
        query (str): The original query.
        answer (str): The generated answer.
    """
    query: str
    answer: str

class Health_Status(BaseModel):
    message: str
    status: str

